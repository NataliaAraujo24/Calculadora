    private void btnRaizCubicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRaizCubicaActionPerformed
        // TODO add your handling code here:
        double num1 =Double.parseDouble(txtnumero1.getText());
        double raiz;
        raiz = Math.pow(num1,1.0/3.0);
        txtresultado.setText(String.valueOf(raiz));
    }//GEN-LAST:event_btnRaizCubicaActionPerformed

    private void btnFactorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFactorialActionPerformed
        // TODO add your handling code here:
        int x = Integer.valueOf(txtnumero1.getText());
        int factorial;
        factorial = 1;
        while(x !=0){
            factorial *=x;
            x--;
            txtresultado.setText(String.valueOf(factorial));
    }
    }//GEN-LAST:event_btnFactorialActionPerformed

    private void btnmoduloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmoduloActionPerformed
        // TODO add your handling code here:
        double num1 = Double.parseDouble(txtnumero1.getText());
        double num2 = Double.parseDouble(txtnumero2.getText());
        
        double modulo = num1% num2;
        txtresultado.setText(String.valueOf(modulo));
                
                
    }//GEN-LAST:event_btnmoduloActionPerformed

    private void btnporcentajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnporcentajeActionPerformed
        // TODO add your handling code here:
        double num1 = Double.parseDouble(txtnumero1.getText());
        double num2 = Double.parseDouble(txtnumero2.getText());
        
        double por = (num1*num2)/100;
        txtresultado.setText(String.valueOf(por));
        
    }//GEN-LAST:event_btnporcentajeActionPerformed

